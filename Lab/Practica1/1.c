#include <stdio.h>

int main (void)
{
  int Acumulador = 0;
  int Numero;
  do
    {
      printf ("Introduzca un numero, introduzca el numero '0' para terminar\n");
      scanf ("%d", &Numero);
      if (Numero != 0)
	{
	  printf ("Se introdujo el numero %d\n", Numero);
	  Acumulador += 1;
	}
    }
  while (Numero != 0);
  printf ("Se introdujeron %d numero(s)\n", Acumulador);
}
